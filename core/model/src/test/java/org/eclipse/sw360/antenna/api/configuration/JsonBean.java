package org.eclipse.sw360.antenna.api.configuration;

import java.util.Objects;

/**
 * A test bean class used to check the JSON serialization capabilities.
 */
public class JsonBean {
    private String name;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        JsonBean jsonBean = (JsonBean) o;
        return getAge() == jsonBean.getAge() &&
                Objects.equals(getName(), jsonBean.getName());
    }

    @Override
    public int hashCode() {
        return Objects.hash(getName(), getAge());
    }
}
